// numeros
let x = 4;
let y = 5;
// backtick string `
// console.log("La suma es:" + x + y);
console.log(`La suma es: ${x + y}`);
console.log(`La resta es: ${x - y}`);
console.log(`La multiplicación es: ${x * y}`);
console.log(`La división es: ${x / y}`);
console.log(`El módulo es: ${x % y}`);

// cadena de caracteres
let a = "hola";
